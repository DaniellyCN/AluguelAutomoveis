    public void LoadTableCliente(){
        Object Colunas[] = {"Nome", "CPF", "CNH", "Rua", "Número", "Bairro", "Cidade", "CEP", "Telefone"};
        DefaultTableModel modelo = new DefaultTableModel(Colunas,0);
        for(int i = 0; i < listaCliente.size(); i++){
            Object linha[] = new Object[]{listaCliente.get(i).getNome(),
            listaCliente.get(i).getCpf(), listaCliente.get(i).getCnh(), listaCliente.get(i).getRua(),
            listaCliente.get(i).getNumeroCasa(), listaCliente.get(i).getBairro(),listaCliente.get(i).getCidade(),
            listaCliente.get(i).getCep(),listaCliente.get(i).getTelefone()};
            
            modelo.addRow(linha);
        }
        tabela_clientes.setModel(modelo);
    };
