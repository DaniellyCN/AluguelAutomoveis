    public void manipulaInterface(String modo){
        switch(modo){
            case "Navegar":
                salvar_btn.setEnabled(false);
                nome_field.setEnabled(false);
                cpf_field.setEnabled(false);
                cnh_field.setEnabled(false);
                rua_field.setEnabled(false);
                numero_field.setEnabled(false);
                bairro_field.setEnabled(false);
                cidade_field.setEnabled(false);
                cep_field.setEnabled(false);
                telefone_field.setEnabled(false);
                novo_btn.setEnabled(true);
                editar_btn.setEnabled(false);
                excluir_btn.setEnabled(false);
                break;
                
            case "Novo":
                salvar_btn.setEnabled(true);
                nome_field.setEnabled(true);
                cpf_field.setEnabled(true);
                cnh_field.setEnabled(true);
                rua_field.setEnabled(true);
                numero_field.setEnabled(true);
                bairro_field.setEnabled(true);
                cidade_field.setEnabled(true);
                cep_field.setEnabled(true);
                telefone_field.setEnabled(true);
                novo_btn.setEnabled(false);
                editar_btn.setEnabled(false);
                excluir_btn.setEnabled(false);
                break;
                
            case "Editar":
               salvar_btn.setEnabled(true);
                nome_field.setEnabled(true);
                cpf_field.setEnabled(true);
                cnh_field.setEnabled(true);
                rua_field.setEnabled(true);
                numero_field.setEnabled(true);
                bairro_field.setEnabled(true);
                cidade_field.setEnabled(true);
                cep_field.setEnabled(true);
                telefone_field.setEnabled(true);
                novo_btn.setEnabled(true);
                editar_btn.setEnabled(false);
                excluir_btn.setEnabled(false);
                break; 
                
            case "Excluir":
                salvar_btn.setEnabled(false);
                nome_field.setEnabled(false);
                cpf_field.setEnabled(false);
                cnh_field.setEnabled(false);
                rua_field.setEnabled(false);
                numero_field.setEnabled(false);
                bairro_field.setEnabled(false);
                cidade_field.setEnabled(false);
                cep_field.setEnabled(false);
                telefone_field.setEnabled(false);
                novo_btn.setEnabled(true);
                editar_btn.setEnabled(false);
                excluir_btn.setEnabled(false);
                break;
                
            default: System.out.println("Modo inválido");
        }
    }

